let fruta = 'naranja';
switch (fruta) {
    case 'manzana':
        console.log("Rojo o verde");
        break;
    case 'plátano':
        console.log("Amarillo");
        break;
    case 'naranja':
        console.log("Naranja");
        break;
    default:
        console.log("Fruta inválida");
}