let calificacion = 'C';
switch (calificacion) {
    case 'A':
        console.log("Excelente");
        break;
    case 'B':
        console.log("Bueno");
        break;
    case 'C':
        console.log("Regular");
        break;
    case 'D':
        console.log("Deficiente");
        break;
    case 'F':
        console.log("Reprobado");
        break;
    default:
        console.log("Calificación inválida");
}